const express = require('express')
const { Router } = express
const router = Router()
const { signUp, logIn, logOut, getAllUsers, createUser, updateUserById, deleteUserById } = require('../controllers/auth.controllers')
const { getAllPhotos, createPhoto, deletePhotoById, updatePhotoById } = require('../controllers/gallery.controllers')
const { getClientOrders, getAllOrders, deleteOrderById, createNewOrder, updateOrderById } = require('../controllers/order.controllers')
const { getAllProducts, createProduct, updateProductById, deleteProductById } = require('../controllers/products.controllers')
const { isAuthenticated, fadeLogs, isAdmin } = require('../middlewares/middlewares')
const Auth = require('../models/Auth')

router
.get('/login', fadeLogs, (req, res) => { res.render('account/login', { user: req.session.user }) })
.get('/signup', fadeLogs, (req, res) => { res.render('account/signup', { user: req.session.user }) })
.get("/logout", logOut)
.get('/', isAuthenticated, getClientOrders)
.get('/administrator/ordenes', [ isAdmin, isAuthenticated ], getAllOrders)
.get('/administrator/ordenes/delete/:id', [ isAdmin, isAuthenticated ], deleteOrderById)
.post('/administrator/ordenes', [ isAdmin, isAuthenticated ], createNewOrder)
.post('/administrator/ordenes/edit/:id', [ isAdmin, isAuthenticated ], updateOrderById)
.get('/administrator/usuarios', [ isAdmin, isAuthenticated ], getAllUsers)
.get('/administrator/usuarios/delete/:id', [ isAdmin, isAuthenticated ], deleteUserById)
.post('/administrator/usuarios', [ isAdmin, isAuthenticated ], createUser)
.post('/administrator/usuarios/edit/:id', [ isAdmin, isAuthenticated ], updateUserById)
.get('/administrator/productos', [ isAdmin, isAuthenticated ], getAllProducts)
.get('/administrator/productos/delete/:id', [ isAdmin, isAuthenticated ], deleteProductById)
.post('/administrator/productos', [ isAdmin, isAuthenticated ], createProduct)
.post('/administrator/productos/edit/:id', [ isAdmin, isAuthenticated ], updateProductById)
.get('/administrator/galeria', [ isAdmin, isAuthenticated ], getAllPhotos)
.get('/administrator/galeria/delete/:id', [ isAdmin, isAuthenticated ], deletePhotoById)
.post('/administrator/galeria', [ isAdmin, isAuthenticated ], createPhoto)
.post('/administrator/galeria/edit/:id', [ isAdmin, isAuthenticated ], updatePhotoById)
.post('/signup', signUp)
.post('/login', logIn)

module.exports = router